export default function makeCreateUser({ createUser }) {
    return async function postUser(httpRequest) {
        try {
            const posted = await createUser(httpRequest.params.token)
            return {
                headers: {
                    'Content-Type': 'application/json',
                },
                statusCode: 201,
                body: posted
            }
        } catch (e) {
            console.log(e)

            return {
                headers: {
                    'Content-Type': 'application/json'
                },
                statusCode: 400,
                body: {
                    error: e.message
                }
            }
        }
    }
}